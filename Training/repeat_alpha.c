#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	c;
	int	rep;

	if (ac <=2)
		write(1, "\n", 1);
	while (av[1][i] != '\0')
	{
		c = av[1][i];
		if (av[1][i] >= 'a' && av[1][i] <= 'z')
		{
			rep = c - 'a';
		}
		if (av[1][i] >= 'A' && av[1][i] <= 'Z')
		{
			rep = c - 'A';
		}
		while (rep >= 0)
		{
			write(1, &c, 1);
			rep--;
		}
		i++;
	}
	write(1, "\n", 1);
}